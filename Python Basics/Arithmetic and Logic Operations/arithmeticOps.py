'''
    Arithmetic Operations play an important role in solving basic problems with numbers
    It is thus essential to know the basic arithmetic operations that Python provides with itself
'''

#Assigning Values
num1 = 13
num2 = 4

#Addition Operation
print("Sum of the numbers is: {}".format(num1 + num2))

#Subtraction Operation
print("Difference of the numbers is {}".format(num1 - num2))

#Product Operation
print("Product of the numbers is {}".format(num1 * num2))

#Division Operation
print("Division of the numbers gives {}".format(num1/num2))

#Floor Division (Quotient of a Division)
print("Quotient of Division of the numbers is {}".format(num1 // num2))

#Modulus Operation (Remainder of a Divsion)
print("Remainder of Division of the numbers is {}".format(num1 % num2))

#Exponetation Operation (Powering up a Number by the Second operand)
print("{0} raised to the power {1} gives {2}".format(num1, num2, num1**num2))

